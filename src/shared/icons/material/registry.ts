export type MaterialIconName = keyof typeof materialIconSymbolIds;

export const MATERIAL_ICON_NAMES = Object.keys(
  materialIconSymbolIds,
) as MaterialIconName[];

export function isMaterialIconName(name: string): name is MaterialIconName {
  return Object.prototype.hasOwnProperty.call(materialIconSymbolIds, name);
}

export function resolveMaterialIconName(name: string): MaterialIconName {
  return isMaterialIconName(name) ? name : "help";
}

export function getMaterialIconSymbolId(name: MaterialIconName): string {
  return materialIconSymbolIds[name];
}
